package de.ostfalia.teamx.snakorino.model;

public class Vector2 {

    public int x, y;

    public Vector2() {
    }

    public Vector2(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
